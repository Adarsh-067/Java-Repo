public class Escape {
    public static void main(String[] args) {
        // Escape Sequence

        System.out.println("Hello world...");
        System.out.println("Hello \"world\"....");
        System.out.println("Hello \\world\\...");
        System.out.println("Hello 'world'...");
        System.out.println("Hello \nworld...");
        System.out.println("Hello \tworld...");
        System.out.println("Hello \bworld...");

    }
}
