public class Arithmetic {
    public static void main(String[] args) {
        int first = 5;
        int second = 3;

        System.out.println("fir + second = " + (first + second));
        System.out.println("fir - second = " + (first - second));
        System.out.println("fir * second = " + (first * second));
        System.out.println("fir / second = " + (first / second));
        System.out.println("fir % second = " + (first % second));

        double fir = 5;
//        double sec = 3;
        double sec = 2;

        System.out.println("\nfir + sec = " + (fir + sec));
        System.out.println("fir - sec = " + (fir - sec));
        System.out.println("fir * sec = " + (fir * sec));
        System.out.println("fir / sec = " + (fir / sec));
        System.out.println("fir % sec = " + (fir % sec));

    }
}
