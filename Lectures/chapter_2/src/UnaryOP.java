public class UnaryOP {
    public static void main(String[] args) {
        // Unary operator

        int  num = 5;
        System.out.println(-num);   // unary operator
        num++;                      // post increment operator
        System.out.println(num);
        ++num;                      // pre increment operator
        System.out.println(num);

        int num1 = 6;
        System.out.println(num1);
        num1--;
        System.out.println(num1);
        --num1;
        System.out.println(num1);
    }
}
