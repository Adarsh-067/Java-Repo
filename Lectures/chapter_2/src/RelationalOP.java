public class RelationalOP {
    public static void main(String[] args) {

        int num = 5;
        int num2 = 6;
        if (num == num2){     // equality operator
            System.out.println("true");
        }

        if (num != num2){    // inequality operator
            System.out.println("true2");
        }

        if (5 > 6){          // greater than operator
            System.out.println("true3");
        }

        if (5 >= 6){         // greater than equal to operator
            System.out.println("true4");
        }

        if (5 < 6){           // smaller than operator
            System.out.println("true5");
        }

        if (5 <= 6){           // smaller than equal to operator
            System.out.println("true6");
        }
    }
}
