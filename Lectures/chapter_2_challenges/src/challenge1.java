public class challenge1 {
    public static void main(String[] args) {

        // Swapping the number
        int first = 2;
        int second = 5;
        int temp;

        System.out.println("first: " + first);
        System.out.println("second: " + second);
        System.out.println("\nswapping.... ");
        temp = first;
        first = second;
        second = temp;

        System.out.println("first: " + first);
        System.out.println("second: " + second);
    }
}